#[derive(Clone, Copy)]
pub enum Language {
    En,
    Ja,
}

#[derive(Clone, Copy)]
pub enum TranslationKey {
    ActionCopy,
    ActionOpen,
    ActionSearchNext,
    ActionSearchPrevious,
    ActionSearchClear,
    ActionResetZoom,
    ActionSwitchLanguage,
    ActionSwitchTheme,
    LabelCurrentFile,
    LabelNoFile,
    LabelSearch,
    LabelSearchResults,
    LabelZoom,
    MessageCopied,
    MessageDropMarkdown,
    MessageEmpty,
    MessageOpenPrompt,
    MessageSearchNoResults,
    NavJumpToHeading,
    NavSections,
    ReloadError,
    ReloadIdle,
    ReloadReloading,
    StatusLoadFailed,
    StatusLoaded,
    StatusNoFile,
    StatusReloadFailed,
    StatusReloadSkipped,
    StatusReloadStarted,
    StatusReloaded,
    StatusUnsupportedFile,
    StatusWatchFailed,
    StatusWorkerFailed,
    ThemeMist,
    ThemeNightOwl,
    ThemeWarmPaper,
}

pub fn tr(language: Language, key: TranslationKey) -> &'static str {
    match language {
        Language::En => en(key),
        Language::Ja => ja(key),
    }
}

